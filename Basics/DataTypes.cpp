#include<iostream>
using namespace std;

int main()
{
    // integer data type
    int a = 5;
    cout << a << endl;


    // Character data type
    char ch = 'd';
    cout<< ch << endl;

    // boolean 
    bool bl = false;
    cout<< bl << endl;

    // float
    float f = 1.2;
    cout<< f << endl; 

    //double
    double d = 1.23455;
    cout<< d << endl;

    // to check the size of the variable
    int size = sizeof(d);
    cout << size << endl;

    
    
    
}